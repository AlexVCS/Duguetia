    <p class="md:w-3/5">
        <a href="https://en.wikipedia.org/wiki/Duguetia">Duguetia</a> is a genus of fruiting plants from the Custard Apple Family (Annonaeceae) which
        is comprised of about 90 species from Central and South America, The Caribbean and West Africa.
        The bulk of the species are native to Brazil, Colombia, Ecuador, Peru and Bolivia. The genus was
        named by European taxonomists for Jacques Joseph Duguet (1649-1733) a French clergyman and
        professor of Theology. Some species are known to indigenous people by the name Araticum.
